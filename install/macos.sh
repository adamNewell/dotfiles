write_safari_pref() {
    if ! defaults write com.apple.Safari "$1" "$2" "$3" 2>/dev/null; then
        defaults write -g "$1" "$2" "$3"
    fi
}

# Function to write global preferences
write_global_pref() {
    defaults write NSGlobalDomain "$1" "$2" "$3"
}

# Privacy: don't send search queries to Apple
